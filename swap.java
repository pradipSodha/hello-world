public class Main{
  public static void main(String args[]){
    int x=2;
    int y=3;
    
    System.out.println("Before swaping :  ");
    System.out.println("x is "+x);
    System.out.println("y is "+y);
    
    //logic 
    x = y;
    y = x;
    
    System.out.println("After swaping :  ");
    System.out.println("x is "+x);
    System.out.println("y is "+y);
    
  }
}
